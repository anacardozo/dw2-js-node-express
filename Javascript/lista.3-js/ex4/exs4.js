function Claro(){
  document.querySelector('#container').style.backgroundColor = "white";
  document.querySelector('h1').style.color = "black";
  document.querySelector('.btn-claro').style.backgroundColor = "gray";
  document.querySelector('.btn-escuro').style.backgroundColor = "gray";
  document.querySelector('.btn-claro').style.color = "white";
  document.querySelector('.btn-escuro').style.color = "white";
}

function Escuro(){
  document.querySelector('#container').style.backgroundColor = "black";
  document.querySelector('h1').style.color = "white";
  document.querySelector('.btn-claro').style.backgroundColor = "white";
  document.querySelector('.btn-escuro').style.backgroundColor = "white";
  document.querySelector('.btn-claro').style.color = "black";
  document.querySelector('.btn-escuro').style.color = "black";
}